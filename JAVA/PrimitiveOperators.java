public class PrimitiveOperators {
    public static void main(String[] args) {
	int beer  = 64;
    int whisky = 23;
    System.out.println(beer + whisky);
    System.out.println();

    int sum1 = 55;
    int sum2 = sum1 - 25;
    int sum3 = sum1 + sum2;
    System.out.println(sum3);
    System.out.println();

    int x = 15; int y = 3;
    int result = x * y;
    System.out.println(x / y);
    System.out.println(result);
    System.out.println();

    int value = 23;
    int res = value % 6; //res = 5
    System.out.println(res);
    System.out.println();
	}
}